/* スペル差分（LCSで一致文字を判定） */
function lcsMark(a,b){
  const al=a.toLowerCase(),bl=b.toLowerCase(),n=al.length,m=bl.length;
  const dp=Array.from({length:n+1},()=>new Array(m+1).fill(0));
  for(let i=1;i<=n;i++)for(let j=1;j<=m;j++)
    dp[i][j]= al[i-1]===bl[j-1]?dp[i-1][j-1]+1:Math.max(dp[i-1][j],dp[i][j-1]);
  const aM=new Array(n).fill(false),bM=new Array(m).fill(false);
  let i=n,j=m;
  while(i>0&&j>0){
    if(al[i-1]===bl[j-1]){aM[i-1]=true;bM[j-1]=true;i--;j--;}
    else if(dp[i-1][j]>=dp[i][j-1])i--;else j--;
  }
  return {aM,bM};
}
function showDiff(user,correct){
  const {aM,bM}=lcsMark(user,correct);
  const u=[...user].map((c,i)=>aM[i]?esc(c):'<span class="x">'+esc(c)+'</span>').join('');
  const c=[...correct].map((c,i)=>bM[i]?'<span class="ok">'+esc(c)+'</span>':'<span class="miss">'+esc(c)+'</span>').join('');
  $('diff').innerHTML=
    '<div class="row2"><span class="lab">あなたの入力</span>'+(user?u:'<span class="x">（空白）</span>')+'</div>'+
    '<div class="row2"><span class="lab">正しいスペル</span>'+c+'</div>';
  $('diff').classList.add('show');
}

